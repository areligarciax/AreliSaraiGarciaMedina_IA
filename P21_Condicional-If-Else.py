# Declaramos la variable edad como entrada de datos tipo entero

edad = int(input("¿Cuántos años tienes?\n"))

# Si la variable edad es mayor o igual a 18, entrará al if

if edad >= 18:
    print("\nPuedes acceder al contenido debido a que eres mayor a 18 años:)")


# En caso contrario de ser menor, entrará al else e imprimirá el resultado
else:
    print("\nAún eres menor de edad, por lo tanto no puedes acceder al contenido:(")